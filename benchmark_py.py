"""Cell 2: The "Standard" Implementation (The Baseline)"""

class StandardAttention(nn.Module):
    def __init__(self, dim, num_heads):
        super().__init__()
        self.num_heads = num_heads
        self.scale = (dim // num_heads) ** -0.5
        self.qkv = nn.Linear(dim, dim * 3, bias=False)
        self.proj = nn.Linear(dim, dim)

    def forward(self, x):
        B, N, C = x.shape
        # Standard manual implementation (Memory heavy)
        qkv = self.qkv(x).reshape(B, N, 3, self.num_heads, C // self.num_heads).permute(2, 0, 3, 1, 4)
        q, k, v = qkv[0], qkv[1], qkv[2]

        attn = (q @ k.transpose(-2, -1)) * self.scale
        attn = attn.softmax(dim=-1)
        x = (attn @ v).transpose(1, 2).reshape(B, N, C)
        return self.proj(x)

print("‚úÖ Standard Attention Model Loaded")

"""Cell 3: The "Codex-Spark Optimized" Implementation"""

class CodexOptimizedAttention(nn.Module):
    def __init__(self, dim, num_heads):
        super().__init__()
        self.num_heads = num_heads
        # Optimization: Removing manual scale calculation, handled by F.scaled_dot_product_attention
        self.qkv = nn.Linear(dim, dim * 3, bias=False)
        self.proj = nn.Linear(dim, dim)

    def forward(self, x):
        B, N, C = x.shape
        qkv = self.qkv(x).reshape(B, N, 3, self.num_heads, C // self.num_heads).permute(2, 0, 3, 1, 4)
        q, k, v = qkv[0], qkv[1], qkv[2]

        # ‚ö°Ô∏è OPTIMIZATION: Using FlashAttention backend (simulated AI optimization)
        # The AI "suggested" using the fused kernel for 2x speedup
        x = F.scaled_dot_product_attention(q, k, v)

        x = x.transpose(1, 2).reshape(B, N, C)
        return self.proj(x)

print("‚úÖ Codex-Spark Optimized Model Loaded")

"""Cell 4: The Benchmark (The Proof)"""

def benchmark(model, x, runs=100):
    torch.cuda.synchronize()
    start = time.time()
    for _ in range(runs):
        with torch.no_grad(): # Disable gradient for inference speed test
            _ = model(x)
    torch.cuda.synchronize()
    end = time.time()
    return (end - start) / runs * 1000 # Convert to ms

